// Implementation without function
console.log(
  `The diagonal of a square with where the length of each side is 9, is ${
    9 * Math.sqrt(2)
  }.`
);

// Implementation using a function
function task_a(a) {
  return a * Math.sqrt(2);
}

console.log(
  `The diagonal of a square with where the length of each side is 9, is ${task_a(9)}.`
);
